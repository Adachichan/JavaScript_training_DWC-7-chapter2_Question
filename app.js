let num1 = 6;
let num2 = 4;
let num3 = 2;

// 足し算
let num4 = num1 + num2 + num3;
alert(num1 + '+' + num2 + '+' + num3 + '=' + num4 + 'です');

// 引き算
let num5 = num1 - num2 - num3;
alert(num1 + '-' + num2 + '-' + num3 + '=' + num5 + 'です');

// 掛け算
let num6 = num1 * num2 * num3;
alert(num1 + '×' + num2 + '×' + num3 + '=' + num6 + 'です');

// 割り算
let num7 = num1 / num3;
alert(num1 + '÷' + num3 + '=' + num7 + 'です');